import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva } from "class-variance-authority";
import { cn } from "@/lib/utils";

const buttonVariants = cva(
    "inline-flex items-center justify-center gap-2 whitespace-nowrap text-sm font-heading font-medium tracking-wider ring-offset-background transition-all duration-300 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
    {
        variants: {
            variant: {
                default: "bg-primary text-primary-foreground hover:opacity-90 rounded-xl shadow-soft hover:shadow-glow",
                destructive: "bg-destructive text-destructive-foreground hover:opacity-90 rounded-xl",
                outline: "border-2 border-primary bg-transparent text-primary hover:bg-primary hover:text-primary-foreground rounded-xl",
                secondary: "bg-secondary text-secondary-foreground hover:opacity-80 rounded-xl",
                ghost: "hover:bg-accent hover:text-accent-foreground rounded-xl",
                link: "text-primary underline-offset-4 hover:underline",
                hero: "bg-gradient-primary text-primary-foreground rounded-full shadow-soft hover:shadow-glow hover:scale-105",
                "hero-outline": "border-2 border-primary/30 bg-transparent text-foreground hover:border-primary hover:bg-accent rounded-full",
            },
            size: {
                default: "h-11 px-6 py-2",
                sm: "h-9 px-4",
                lg: "h-14 px-10 text-base",
                icon: "h-10 w-10",
            },
        },
        defaultVariants: {
            variant: "default",
            size: "default",
        },
    }
);

const Button = React.forwardRef(
    ({ className, variant, size, asChild = false, ...props }, ref) => {
        const Comp = asChild ? Slot : "button";
        return (
            <Comp
                className={cn(buttonVariants({ variant, size, className }))}
                ref={ref}
                {...props}
            />
        );
    }
);
Button.displayName = "Button";

export { Button, buttonVariants };
